1. What ?
--------------------------------------------------------------

JoomDOCLink is an Joomla! editor plugin that allows the insertion of
links to JoomDOC files using a popup dialog.
This is based on original DOCLink for DOCMan by JoomlaTools.

Features :

	- Insert links to JoomDOC documents and categories
	- Add file date/size/icon
	- International language support (Language packs) currenly UK
	- Multiple browser support, currently Mozilla, MSIE and FireFox.

NOTE! This version will only work with Joomla! v1.5


2. Installation
--------------------------------------------------------------

Use Joomla's built in mambot installer.
Don't forget to publish the plugin.


3. Which editors are supported ?
--------------------------------------------------------------

DOCLink supports :

* TinyMCE
* HTMLArea3 XTD
* WysiwygPro
* TMEdit
* mosCE



